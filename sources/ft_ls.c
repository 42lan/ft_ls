#include "ft_ls.h"

int			ft_ls(int ac, char **av)
{
	int		i;

	i = -1;
	if (ac == 0)
		ft_listdir(".");
	while (++i < ac)
	{
		ft_listdir(av[i]);
		if (i < (ac - 1))
			ft_printf("\n");
	}
	return (1);
}
